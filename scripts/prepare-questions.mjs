import { readdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const privateDirectory = join(projectRoot, "src", "data", "questions", "private");
const generatedFile = join(projectRoot, "src", "data", "questions", "private.generated.ts");

async function findPrivateJsonFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...await findPrivateJsonFiles(fullPath));
    } else if (entry.isFile() && entry.name.endsWith(".json") && !entry.name.endsWith(".example.json")) {
      files.push(fullPath);
    }
  }

  return files.sort();
}

const privateFiles = await findPrivateJsonFiles(privateDirectory);
const privateQuestions = [];

for (const filePath of privateFiles) {
  let parsed;
  try {
    parsed = JSON.parse(await readFile(filePath, "utf8"));
  } catch (error) {
    throw new Error(`${relative(projectRoot, filePath)} をJSONとして読み込めません。`, { cause: error });
  }

  if (!Array.isArray(parsed)) {
    throw new Error(`${relative(projectRoot, filePath)} のルートは問題配列にしてください。`);
  }
  privateQuestions.push(...parsed);
}

const generatedSource = `// This file is generated by scripts/prepare-questions.mjs and is intentionally ignored by Git.\nimport type { Question } from "../../types";\n\nexport const privateQuestions = ${JSON.stringify(privateQuestions, null, 2)} satisfies Question[];\n`;
await writeFile(generatedFile, generatedSource, "utf8");

console.log(`Prepared ${privateQuestions.length} private question(s) from ${privateFiles.length} local file(s).`);
